def somar(a, b):
    """ retorna a soma de dois valores"""
    return a + b
    
def multiplicar(a, b):
    """ retorna a multiplicação de dois valores"""
    return a * b
    
def subtrair(a, b):
    """retorna a subtração de valor a - b"""
    return a - b
 
def dividir(a, b):
    """retorna a divisão de dois numeros"""
    return a / b