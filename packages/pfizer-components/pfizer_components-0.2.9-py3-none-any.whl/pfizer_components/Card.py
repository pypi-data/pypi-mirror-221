class Card(dbc.Card):
    """A Card component.
    Component for creating Bootstrap Card. Use in conjunction with CardBody,
    CardImg, CardLink, CardHeader and CardFooter. Can also be used in
    conjunction with CardColumns, CardDeck, CardGroup for different layout
    options.

    Keyword arguments:

        - children (a list of or a singular dash component, string or number; optional):
            The children of this component.

        - id (string; optional):
            The ID of this component, used to identify dash components in
            callbacks. The ID needs to be unique across all of the components
            in an app.

        - body (boolean; optional):
            Apply the `card-body` class to the card, so that there is no need
            to also include a CardBody component in the children of this Card.
            Default: False.

        - className (string; optional):
            **DEPRECATED** Use `class_name` instead.  Often used with CSS to
            style elements with common properties.

        - class_name (string; optional):
            Often used with CSS to style elements with common properties.

        - color (string; optional):
            Card color, options: primary, secondary, success, info, warning,
            danger, light, dark or any valid CSS color of your choice (e.g. a
            hex code, a decimal code or a CSS color name). Default is light.

        - inverse (boolean; optional):
            Invert text colours for use with a darker background.

        - key (string; optional):
            A unique identifier for the component, used to improve performance
            by React.js while rendering components See
            https://reactjs.org/docs/lists-and-keys.html for more info.

        - loading_state (dict; optional):
            Object that holds the loading state object coming from
            dash-renderer.

            `loading_state` is a dict with keys:

            - component_name (string; optional):
                Holds the name of the component that is loading.

            - is_loading (boolean; optional):
                Determines if the component is loading or not.

            - prop_name (string; optional):
                Holds which property is loading.

        - outline (boolean; optional):
            Apply color styling to just the border of the card.

        - style (dict; optional):
            Defines CSS styles which will override styles previously set."""

    def __init__(self, children=None, **card_props):
        # Create a shallow copy of component props if it exists, otherwise create an empty dictionary
        card_props = card_props.copy() if card_props else {}
        # Extract the 'style' property if it exists and remove it from component props
        style = card_props.pop('style', None)
        default_style = {}
        # If style is not None, update the default_style dictionary with the contents of the style dictionary
        if style is not None:
            default_style.update(style)
        card_props['style'] = default_style
        # Call the __init__ method of the parent class with the children and component props arguments
        super().__init__(children=children, **card_props)


